char	ft_putchar(char c);

void	ft_is_negative(int n)
{
	int		i;
	char	c;

	c = '0';
	i = 1;
	while (i <= 10)
	{
		if (n >= 0)
		{
			c = 'P';
			ft_putchar(c);
		}
		else
		{
			c = 'N';
			ft_putchar(c);
		}
		i = 20;
	}
}
