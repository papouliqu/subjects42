char	ft_putchar(char c);

void	ft_print_combn(void)
{
	char	tb[4];

	tb[0] = 0;
	tb[1] = 1;
	tb[2] = 2;
	tb[3] = 3;
	while (!((tb[0] == 9) && (tb[1] == 9)))
	{
		ft_putchar(tb[0] + 48);
		ft_putchar(tb[1] + 48);
		ft_putchar(' ');
		ft_putchar(tb[2] + 48);
		ft_putchar(tb[3] + 48);
		if (!((tb[0] == 9) && (tb[1] == 8)))
		{
			ft_putchar(',');
		}
		if (!((tb[0] == 9) && (tb[1] == 8)))
		{
			ft_putchar(' ');
		}
		tb[3]++;
		if ((tb[3] = tb[3] % 10) == 0)
		{
			tb[2] = (tb[2] + 1);
		}
		if (tb[2] == 10)
		{
			tb[2] = tb[0];
			tb[1]++;
			if ((tb[1] = tb[1] % 10) == 0)
			{
				tb[0]++;
			}
			tb[3] = tb[1] + 1;
		}
	}
}
