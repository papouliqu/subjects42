int		ft_sqrt(int nb)
{
	int x;
	int x0;
	int r;

	x = 1;
	x0 = nb;
	while (x < nb && x != x0)
	{
		x0 = x;
		x = (x + nb / x) / 2;
		r = x - x0;
		if (r == 1 || r == -1)
			return (0);
	}
	if (x * x == nb)
		return (x);
	else
		return (0);
}
