int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] != '\0' && s2[i] != '\0') && (i < n))
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	if ((s1[0] == '\0') || (s2[0] == '\0'))
		return (s1[0] - s2[0]);
	else
		return (s1[i] - s2[i]);
}
