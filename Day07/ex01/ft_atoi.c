#include <stdio.h>

int		ft_atoi(char *s)
{
	int		i;
	int		n;
	int		signe;

	n = 0;
	i = 0;
	signe = 1;
	while (*(s + i) == ' ')
		i++;
	if (*(s + i) == '+' || *(s + i) == '-')
	{
		signe = (*(s + i) == '+') ? 1 : -1;
		i++;
		if (*(s + i) == '+' || *(s + i) == '-')
			return (0);
	}
	while ((*(s + i) != '\0') && (*(s + i) >= '0' && *(s + i) <= '9'))
	{
		n = 10 * n + (*(s + i) - '0');
		i++;
	}
	n = n * signe;
	return (n);
}
